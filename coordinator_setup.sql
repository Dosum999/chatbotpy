-- 샘플 조회 쿼리 (상위 5명)
SELECT 
    c.coordinator_id,
    c.name,
    c.gender,
    c.age,
    c.address,
    c.care_index,
    c.phone,
    GROUP_CONCAT(DISTINCT ar.region_name) as regions,
    GROUP_CONCAT(DISTINCT cl.language_name) as languages,
    GROUP_CONCAT(DISTINCT cc.certification_name) as certifications,
    ce.experience_description as experiences
FROM coordinators c
LEFT JOIN available_regions ar ON c.coordinator_id = ar.coordinator_id
LEFT JOIN coordinator_languages cl ON c.coordinator_id = cl.coordinator_id
LEFT JOIN coordinator_certifications cc ON c.coordinator_id = cc.coordinator_id
LEFT JOIN coordinator_experiences ce ON c.coordinator_id = ce.coordinator_id
WHERE c.is_active = TRUE
GROUP BY c.coordinator_id
ORDER BY c.care_index DESC
LIMIT 5;