-- 코디네이터 추천 시스템을 위한 MySQL 테이블 생성 및 더미 데이터 삽입 스크립트

-- 기존 테이블 삭제 (순서 중요)
DROP TABLE IF EXISTS coordinator_experiences;
DROP TABLE IF EXISTS coordinator_certifications;
DROP TABLE IF EXISTS coordinator_languages;
DROP TABLE IF EXISTS available_regions;
DROP TABLE IF EXISTS coordinators;

-- 메인 코디네이터 테이블 생성
CREATE TABLE coordinators (
    coordinator_id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    gender ENUM('MALE', 'FEMALE') NOT NULL,
    age INT NOT NULL,
    phone VARCHAR(20) NOT NULL,
    email VARCHAR(100),
    address VARCHAR(200) NOT NULL,
    care_index DECIMAL(3,1) NOT NULL DEFAULT 0.0,
    registration_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    is_active BOOLEAN DEFAULT TRUE,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_care_index (care_index),
    INDEX idx_gender (gender),
    INDEX idx_age (age),
    INDEX idx_address (address),
    INDEX idx_active (is_active)
);

-- 활동 지역 테이블 생성
CREATE TABLE available_regions (
    id INT AUTO_INCREMENT PRIMARY KEY,
    coordinator_id INT NOT NULL,
    region_name VARCHAR(100) NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (coordinator_id) REFERENCES coordinators(coordinator_id) ON DELETE CASCADE,
    INDEX idx_coordinator_id (coordinator_id),
    INDEX idx_region_name (region_name)
);

-- 보유 언어 테이블 생성
CREATE TABLE coordinator_languages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    coordinator_id INT NOT NULL,
    language_name VARCHAR(50) NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (coordinator_id) REFERENCES coordinators(coordinator_id) ON DELETE CASCADE,
    INDEX idx_coordinator_id (coordinator_id)
);

-- 보유 자격증 테이블 생성
CREATE TABLE coordinator_certifications (
    id INT AUTO_INCREMENT PRIMARY KEY,
    coordinator_id INT NOT NULL,
    certification_name VARCHAR(100) NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (coordinator_id) REFERENCES coordinators(coordinator_id) ON DELETE CASCADE,
    INDEX idx_coordinator_id (coordinator_id)
);

-- 경력 사항 테이블 생성
CREATE TABLE coordinator_experiences (
    id INT AUTO_INCREMENT PRIMARY KEY,
    coordinator_id INT NOT NULL,
    experience_description TEXT NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (coordinator_id) REFERENCES coordinators(coordinator_id) ON DELETE CASCADE,
    INDEX idx_coordinator_id (coordinator_id)
);

